package Modal;

public class Ghe {
    private int idGhe;

    private String tenGhe;

    public Ghe(int idGhe, String tenGhe) {
        this.idGhe = idGhe;
        this.tenGhe = tenGhe;
    }

    public int getIdGhe() {
        return idGhe;
    }

    public void setIdGhe(int idGhe) {
        this.idGhe = idGhe;
    }

    public String getTenGhe() {
        return tenGhe;
    }

    public void setTenGhe(String tenGhe) {
        this.tenGhe = tenGhe;
    }
}
